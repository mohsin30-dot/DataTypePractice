public class StringsChars {
    public static void main(String[] args) {

        // a string data type representing textual data in computer programs
        // A String in java is a sequence of characters
        // A char is a single character
        // Strings are enclosed by double quotes


        String word = "England";
        char a = word.charAt(0); // the charAt() method returns the char value
        char b = word.charAt(1);
        char c = word.charAt(2);
        char d = word.charAt(3);
        char e = word.charAt(4);

        System.out.println(c);
        System.out.println(d);
        System.out.println(a);
        System.out.println(b);
        System.out.println(e);
    }
}
